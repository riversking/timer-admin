import { postData } from '../../libs/fetchData'
import { setToken, getToken } from '@/libs/util'

export default {
  state: {
    userName: '',
    userId: '',
    avatorImgPath: '',
    token: getToken(),
    access: '',
    hasGetInfo: false
  },
  actions: {
    // 登录
    async userLogin({commit}, obj) {
      try {
        let res = await postData(`/user/login`, obj).catch(err => {
          // commit('GLOBAL_ERR', err, {root: true})
        })
        console.log("res",res)
        let resUserInfo
        switch (res.status) {
          case 200:
            setToken(res.data.access_token)
            commit('SET_ACCESS_TOKEN', res.data.access_token)
            commit('SET_REFRESH_TOKEN', res.data.refresh_token)
            commit('SET_PERMISSIONS', resUserInfo.data.data.permissions)
            break
          default:
            break
        }

        return resUserInfo.data
      } catch (error) {
        console.log('error: ', error)
      }
    },
    // 退出登录
    handleLogOut ({ state, commit }) {
      return new Promise((resolve, reject) => {
        logout(state.token).then(() => {
          commit('setToken', '')
          commit('setAccess', [])
          resolve()
        }).catch(err => {
          reject(err)
        })
        // 如果你的退出登录无需请求接口，则可以直接使用下面三行代码而无需使用logout调用接口
        // commit('setToken', '')
        // commit('setAccess', [])
        // resolve()
      })
    },
    // 获取用户相关信息
    getUserInfo ({ state, commit }) {
      return new Promise((resolve, reject) => {
        try {
          getUserInfo(state.token).then(res => {
            const data = res.data
            commit('setAvator', data.avator)
            commit('setUserName', data.name)
            commit('setUserId', data.user_id)
            commit('setAccess', data.access)
            commit('setHasGetInfo', true)
            resolve(data)
          }).catch(err => {
            reject(err)
          })
        } catch (error) {
          reject(error)
        }
      })
    }
  },
  mutations: {
    setAvator (state, avatorPath) {
      state.avatorImgPath = avatorPath
    },
    setUserId (state, id) {
      state.userId = id
    },
    setUserName (state, name) {
      state.userName = name
    },
    setAccess (state, access) {
      state.access = access
    },
    setToken (state, token) {
      state.token = token
      setToken(token)
    },
    setHasGetInfo (state, status) {
      state.hasGetInfo = status
    }
  },
}
